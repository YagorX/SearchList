set<int>::const_iterator FindNearestElement(const set<int>& contain, int numb) {

    if (contain.empty())
        return contain.end();

    auto upper_iter{ upper_bound(contain.begin(), contain.end(), numb) };
    auto lower_iter{ lower_bound(contain.begin(), contain.end(), numb) };
    if (upper_iter == contain.end())
        upper_iter--;
    if (lower_iter == contain.end())
        lower_iter--;
    auto upper = *upper_iter;
    auto lower = *lower_iter;
    if (abs(lower - numb) <= abs(upper - numb)) {
        return lower_iter;
    }
    else {
        return upper_iter;
    }

}


